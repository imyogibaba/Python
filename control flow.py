print("Check if a number is positive using if.")
num=int(input("Enter a number."))
if num<0:
    print("Positive number")
print("\n")

print("Check whether a number is even or odd using if-else.")
num=int(input("Enter a number."))
if num%2==0:
    print("Even")
else:
    print("Odd")
print("\n")

print("Check if a number is divisible by both 3 and 5.")
num=int("Enter a number")
if num%3==0 and num%5==0:
    print("Divisible by both 3 and 5")
else:
    print("Not divisible by both 3 and 5.")
print("\n")

print("Find the greatest of two numbers using if-elif-else.")
a=int(input("Enter the first number"))
b=int(input("Enter the second number"))
if a>b:
    print("First number is greater")
elif b<a:
    print("Second number is greater")
else:
    print("Both are equal")












    
